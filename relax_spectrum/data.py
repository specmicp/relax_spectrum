import numpy as np

__all__ = ["Data", "simple_csv_reader"]

class Data:
    """A simple class containing the experimental spectrum."""
    def __init__(self, x, y):
        self._x = x
        self._y = y

    @property
    def x(self):
        return self._x

    @property
    def y(self):
        return self._y

def simple_csv_reader(filename, delimiter=None):
    """A simple reader to obtain a data in text format.

    First column is the spectrum x-axis.
    Second column is the spectrum intensity.

    For different format, your should create your own reader.
    """
    data = np.genfromtxt(filename, delimiter=delimiter)
    return Data(data[:,0], data[:,1])